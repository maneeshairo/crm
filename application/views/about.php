<h2>
    About FMS-endpoint
</h2>
<p>FMS-endpoint is a basic Open311 server with a straightforward database of problem reports.</p>

<ul>
  <li>
    PHP fuelled by <a href="http://codeigniter.com/">CodeIgniter<a>
  </li>
  <li>
    implementation of <a href="http://wiki.open311.org/GeoReport_v2">Open311 GeoReport v2</a> by Philip Ashlock</li>
  <li>
    database interface powered by <a href="http://www.grocerycrud.com/">grocery CRUD</a>
    copyright &copy; 2010 - 2012  John Skoumbourdis, distributed under GPL v3/MIT licenses
  </li>
  <li>
    user auth by <a href="https://github.com/benedmunds/CodeIgniter-Ion-Auth">Ion Auth 2</a> by Ben Edmunds,
    released under the Apache License v2.0
  </li>
  <li>
    YAML parsed with <a href="http://spyc.sourceforge.net">spyc</a>
    (c) 2005-2006 Chris Wanstrath, 2006-2011 Vlad Andersen,
    released under the <a href="http://www.opensource.org/licenses/mit-license.php">MIT License</a>
</ul>

<p>
    Put together by <a href="http://www.mysociety.org">mySociety</a>, who also wrote 
    <a href="http://code.fixmystreet.com">the FixMyStreet platform</a> &mdash; 
    free, internationalised, open source software built to help people run websites for reporting 
    common street problems, like potholes and broken street lights.
</p>

<p>
    FMS-endpoint should be useful if you want to deploy FixMyStreet but don't have a handy
    backend for it to report into. It's basic but that's deliberate:  it's a great place to 
    start if you want to build something bigger and better!
</p>
